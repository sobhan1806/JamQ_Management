      try {
        FormData formData = FormData.fromMap({
          "UserName":UserName,
        });
        Response response = await Dio().post("http://jamq.ir:3000/Management/GetPanelUserByUserName",options: Options(contentType: 'multipart/form-data'),data:formData);
