  GetAwardById() async{
    print('GetAwardById Run...');
    Id = widget.IdResponse;
    try {
      FormData formData = FormData.fromMap({
        "id":Id,
      });
      Response response = await Dio().post("http://jamq.ir:3000/Mainapp/GetAwardById",options: Options(contentType: 'multipart/form-data'),data:formData);
      if(response.data.toString() == 'Awards Does Not Exist!!!'){
        Alert(
          context: context,
          type: AlertType.none,
          title: "پیغام",
          desc: "!!!کلمه ای وجود ندارد",
          buttons: [
            DialogButton(
              child: Text(
                "تایید",
                style: TextStyle(color: Colors.black, fontSize: 18, fontFamily: 'IRANSans'),
              ),
              onPressed: () => Navigator.of(context).pushAndRemoveUntil(
                  MaterialPageRoute(builder: (context) =>
                  new Directionality(textDirection: TextDirection.rtl, child: Home.none())),(Route<dynamic> route) => false),
              color: Color(0xffD3D3D3),
            )
          ],
        ).show(); // Message
        print('response: '+response.data);
      }else{
        awardsInformation = response.data;
        Type = awardsInformation["AwType"];
        Amount = awardsInformation["AwAmount"];
        FillInfo();
        return awardsInformation;
      }
    } catch (e) {
      Alert(
        context: context,
        type: AlertType.none,
        title: "پیغام",
        desc: "!!!ارتباط با سرور برقرار نیست",
        buttons: [
          DialogButton(
            child: Text(
              "تایید",
              style: TextStyle(color: Colors.black, fontSize: 18, fontFamily: 'IRANSans'),
            ),
            onPressed: () => Navigator.of(context).pushAndRemoveUntil(
                MaterialPageRoute(builder: (context) =>
                new Directionality(textDirection: TextDirection.rtl, child: Home.none())),(Route<dynamic> route) => false),
            color: Color(0xffD3D3D3),
          )
        ],
      ).show(); // Message
      print(e);
    }
  }
